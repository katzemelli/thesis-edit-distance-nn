# 1) SYNTHETIC score distribution — recomputed from colab29's EXACT training generator
#    (cell 7: MIN_LEN=50, MAX_LEN=200, sub/ins/del each uniform; cell 52 recipe;
#     label = norm_lev on the realised pair). More samples => smoother estimate of the
#     SAME generator distribution; shape is seed-stable.
# ==========================================================================================
AA_ALPHABET = "ACDEFGHIKLMNPQRSTVWY"
MIN_LEN, MAX_LEN = 50, 200
_rng = np.random.default_rng(0)

def rand_seq(abc):
    L = int(_rng.integers(MIN_LEN, MAX_LEN + 1))
    return "".join(_rng.choice(list(abc), size=L))

def perturb(seq, k, abc):
    s, abc = list(seq), list(abc)
    for _ in range(k):
        if len(s) == 0:
            op = "ins"
        elif len(s) >= MAX_LEN:
            op = _rng.choice(["sub", "del"])
        else:
            op = _rng.choice(["sub", "ins", "del"])
        if op == "sub":
            i = _rng.integers(0, len(s)); s[i] = _rng.choice([c for c in abc if c != s[i]])
        elif op == "ins":
            i = _rng.integers(0, len(s) + 1); s.insert(i, _rng.choice(abc))
        else:
            i = _rng.integers(0, len(s)); del s[i]
    return "".join(s)

def norm_lev(a, b):
    L = max(len(a), len(b))
    return 1.0 if L == 0 else 1.0 - RFLev.distance(a, b) / L

def synthetic_score_density(n=120_000, nbins=80):
    sc = np.empty(n)
    for i in range(n):
        sd = rand_seq(AA_ALPHABET); L = len(sd)
        t = float(_rng.uniform(0, 1)); k = max(0, int(round((1 - t) * L)))
        sc[i] = norm_lev(sd, perturb(sd, k, AA_ALPHABET))
    edges = np.linspace(0, 1, nbins + 1)
    dens, _ = np.histogram(sc, bins=edges, density=True)
    return dens, float(np.median(sc))

