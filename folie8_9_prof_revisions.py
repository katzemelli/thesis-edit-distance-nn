# FIGURE C — Folie 9: letter frequency, per-alphabet RANK-sorted (most->least frequent)
# ==========================================================================================
def fig_C():
    fig, ax = plt.subplots(figsize=(9.2, 5.2))
    ranks = np.arange(1, 21)
    order = {"synthetic": ["synthetic", "AA", "3Di", "SS"]}  # draw order (SS last: shortest)
    for k in ["synthetic", "AA", "3Di", "SS"]:
        f = np.array(LFREQ[k], float)
        idx = np.argsort(-f)                       # descending by this alphabet's own frequency
        f_sorted = f[idx]
        used = int((f > 0).sum())
        xr = ranks[:used]; yr = f_sorted[:used]     # only plot letters actually used
        ax.plot(xr, yr, color=COL[k], lw=2.2, ls=LSTY[k], marker=MARK[k], ms=5,
                label=f"{k}  ({used} letters)")
        # annotate the top-3 most frequent letters (skip synthetic: uniform => ordering is noise).
        # AA labels go BELOW its markers, SS/3Di ABOVE, so the three alphabets don't collide at rank 3.
        if k != "synthetic":
            dy, va = (-13, "top") if k == "AA" else (8, "bottom")
            for r in range(min(3, used)):
                ax.annotate(LETTERS[idx[r]], (xr[r], yr[r]), textcoords="offset points",
                            xytext=(0, dy), ha="center", va=va, fontsize=8.5,
                            color=COL[k], fontweight="bold")
    ax.axhline(1 / 20, ls=":", lw=1, color="0.5")
    ax.text(20.2, 1 / 20, "uniform (1/20)", fontsize=8, color="0.5", va="center")
    ax.set_xticks(ranks)
    ax.set_xlabel("letter rank within alphabet  (1 = most frequent)")
    ax.set_ylabel("frequency")
    ax.set_title("Letter-frequency profiles, each alphabet sorted most→least frequent")
    ax.set_xlim(0.5, 20.8); ax.legend(frameon=False)
    _despine(ax); fig.tight_layout()
    p = os.path.join(OUTDIR, "C_folie9_letterfreq_ranksorted.png")
    fig.savefig(p, dpi=160, bbox_inches="tight"); plt.close(fig); return p

