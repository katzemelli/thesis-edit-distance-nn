# FIGURE D — Folie 9: transition heatmaps, per-alphabet RANK-sorted rows/cols (comparable)
# ==========================================================================================
def fig_D():
    # white-at-chance -> red colormap (matches the deck's transition panels)
    reds = LinearSegmentedColormap.from_list("whitered", ["#ffffff", "#f4c9b8", "#d6604d", "#8b1a1a"])
    fig, axes = plt.subplots(1, 4, figsize=(16.5, 4.6), constrained_layout=True)
    for ax, k in zip(axes, ["synthetic", "AA", "3Di", "SS"]):
        f = np.array(LFREQ[k], float)
        idx = np.argsort(-f)                       # sort by this alphabet's own frequency
        M = np.array(TRANS[k], float)[np.ix_(idx, idx)]
        labels = [LETTERS[i] for i in idx]
        im = ax.imshow(M, vmin=0, vmax=1, cmap=reds, aspect="equal")
        ax.set_title(k, color=COL[k], fontweight="bold")
        ax.set_xticks(range(20)); ax.set_yticks(range(20))
        ax.set_xticklabels(labels, fontsize=6); ax.set_yticklabels(labels, fontsize=6)
        ax.set_xlabel("next letter (freq-sorted)")
        if k == "synthetic":
            ax.set_ylabel("current letter (freq-sorted)")
    cb = fig.colorbar(im, ax=axes, shrink=0.85, label="P(next | current)")
    cb.ax.axhline(0.05, color="0.35", lw=1.0)                    # uniform 20-letter chance
    cb.ax.text(1.9, 0.05, "chance\n(0.05)", fontsize=7, color="0.35", va="center")
    fig.suptitle("Transition probability P(next | current) — rows/cols sorted by letter frequency "
                 "(most→least). SS collapses onto its 3-letter self-transition block.",
                 fontsize=11)
    p = os.path.join(OUTDIR, "D_folie9_transition_ranksorted.png")
    fig.savefig(p, dpi=160, bbox_inches="tight"); plt.close(fig); return p

